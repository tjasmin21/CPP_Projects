#ifndef _EXCEPTIONS_H_
#define _EXCEPTIONS_H_

// includes ///////////////////////////////////////////////////////////////////
#include <string>
using namespace std;


///////////////////////////////////////////////////////////////////////////////
// class:   CError
// purpose: base class for application exceptions
///////////////////////////////////////////////////////////////////////////////
class CError
{
public:
	CError(string msg) : err_message(msg) {};
	string& GetMessage(void) {return err_message;};

private:
	string err_message;
};


///////////////////////////////////////////////////////////////////////////////
// class:   CErrorFileID
// purpose: used for exception handling on file ID check
///////////////////////////////////////////////////////////////////////////////
class CErrorFileID : public CError
{
public:
	CErrorFileID(string msg) : CError(msg) {};
};


///////////////////////////////////////////////////////////////////////////////
// class:   CErrorFigureType
// purpose: used for exception handling on figure type check
///////////////////////////////////////////////////////////////////////////////
class CErrorFigureType : public CError
{
public:
	CErrorFigureType(string msg) : CError(msg) {};
};


#endif // _EXCEPTIONS_H_ //////////////////////////////////////////////////////
